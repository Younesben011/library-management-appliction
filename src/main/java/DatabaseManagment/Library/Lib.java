package DatabaseManagment.Library;

public class Lib {
    private String id ;
    private boolean state;

    public Lib(String id, boolean state) {
        this.id = id;
        this.state = state;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public boolean isState() {
        return state;
    }

    public void setState(boolean state) {
        this.state = state;
    }
}
